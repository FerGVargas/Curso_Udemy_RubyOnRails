# Relacionais

# maior >   
# maior OU igual >=

if 1 > 3
    puts 'a'
else
    put 'b'
end

# menor <  
# menor OU igual <=

if 1 < 2
    puts 'a'
else
    put 'b'
end


# igua ==

if 1 == 1
    puts 'a'
else
    put 'b'
end


# diferente !=

if 1 == 1
    puts 'a'
else
    put 'b'
end
